def to_code_data(data):
    ret = []
    for i in range(0, len(data), 2):
        pix = data[i].replace(' ', '')
        pix = pix.replace('.', '0')
        mp = []
        for c in pix:
            mp.append(int(c, 20))
        delay = data[i+1]
        ret.append(mp)
        ret.append(delay)
    return ret

